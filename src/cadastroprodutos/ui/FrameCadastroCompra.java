    private void btProcurarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarProdutoActionPerformed
        int codProduto = Integer.parseInt(tfCodigoProcurar.getText());
        int quantidade = Integer.parseInt(tfQuantidade.getText());
        
            Produto p = pc.obterProduto(codProduto);

            if(p == null) {
                JOptionPane.showMessageDialog(this, 
                        "Produto não encontrado!");
                tfQuantidade.setText("");
                tfCodigoProcurar.setText("");
                tfCodigoProcurar.requestFocus();
                return;
            }
            //adiciona o produto a compra
            compra.adicionar(p, quantidade);
            
            //atualiza a tabela itens de compra
            Object[] dados = new Object[5];
                
            dados[0] = codProduto;
            dados[1] = p.getDescricaoProd();
            dados[2] = p.getPrecoVenda();
            dados[3] = quantidade;
            dados[4] = quantidade * p.getPrecoVenda();
                
            modeloTabela.addRow(dados);
            modeloTabela.fireTableDataChanged();
            
            tfTotalCompra.setText(
                    Double.toString(compra.getTotal()));
            
            tfQuantidade.setText("");
            tfCodigoProcurar.setText("");
            tfCodigoProcurar.requestFocus();  
        
    }//GEN-LAST:event_btProcurarProdutoActionPerformed

