    private void limpar() {
        //limpar o formulario
        compra = new Compra();
        modeloTabela = new DefaultTableModel();
        modeloTabela.addColumn("Código produto");
        modeloTabela.addColumn("Nome do produto");
        modeloTabela.addColumn("Preço individual");
        modeloTabela.addColumn("Quantidade");
        modeloTabela.addColumn("Preço Total");
        
        tbItensCompra.setModel(modeloTabela);
        
        tfDataCompra.setText(compra.getData());
        
        tfCodigoProcurar.setText("");    
    }    
    
